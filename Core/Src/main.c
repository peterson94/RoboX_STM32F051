/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM16_Init();
  MX_TIM15_Init();
  MX_USART1_UART_Init();
  MX_ADC_Init();
  MX_TIM17_Init();
  /* USER CODE BEGIN 2 */

  //Intro
  //HAL_Delay(2000);

  // Motor control PWM start
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);

  // UART receive start
  HAL_UART_Receive_DMA(&huart1, cmd, 3);

  // BAT Measure timer start
  HAL_TIM_Base_Start_IT(&htim16);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  STATUS = Status_Check();

	  switch (STATUS)
	  {
	  	  case 0:
	  		  // Not going and not needed
	  		  break;

		  case 1:
			  // Not going but needed
			  if(FLAG_TIMEOUT)
			  {
				  HAL_TIM_Base_Stop_IT(&htim15);
				  htim15.Instance->CNT = 0x0;
			  }

			  MOTOR = Motor_Set(cmd[1]);

			  if (MOTOR == 1)
			  {
				  HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_1);
				  HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_2);
				  HAL_TIM_Base_Start_IT(&htim15);
			  }

			  FLAG_UART_1 = 0x00;
			  HAL_UART_Receive_DMA(&huart1, cmd, 3);

			  if(FLAG_TIMEOUT)
			  {
				  FLAG_TIMEOUT= 0x00;
				  HAL_TIM_Base_Start_IT(&htim15);
			  }

			  break;

		  case 2:
			  // Going and needed
			  if (FLAG_UART_1)
			  {
				  HAL_TIM_Base_Stop_IT(&htim15);
				  htim15.Instance->CNT = 0x0;

				  MOTOR = Motor_Set(cmd[1]);

				  // We want to receive the next command
				  FLAG_UART_1 = 0x00;
				  HAL_UART_Receive_DMA(&huart1, cmd, 3);

				  // And let us start timeouter again
				  FLAG_TIMEOUT = 0x00;
				  HAL_TIM_Base_Start_IT(&htim15);
			  }

			  wheel_1.RPM = Speed_Calc(&wheel_1);
			  wheel_2.RPM = Speed_Calc(&wheel_2);

			  Speed_Control(&htim1, base, &wheel_1, &wheel_2);

			  break;

		  case 3:
			  // Going but not needed (or any error case)

			  MOTOR = Motor_Set('5');
			  Speed_Control(&htim1, 0, &wheel_1, &wheel_2);
			  HAL_TIM_IC_Stop_IT(&htim2, TIM_CHANNEL_1);
			  HAL_TIM_IC_Stop_IT(&htim2, TIM_CHANNEL_2);
			  HAL_TIM_Base_Stop_IT(&htim15);

			  htim2.Instance->CNT = 0x0;
			  htim15.Instance->CNT = 0x0;

			  wheel_1.RPM = 0;
			  wheel_2.RPM = 0;

			  cmd[0]='0';
			  cmd[1]='0';
			  cmd[2]='0';

			  FLAG_MEAS_2 = 0x00;
			  wheel_1.IC_Val_1 = 0;
			  wheel_1.IC_Val_0 = 0;

			  FLAG_MEAS_1 = 0x00;
			  wheel_2.IC_Val_1 = 0;
			  wheel_2.IC_Val_0 = 0;

			  FLAG_TIMEOUT = 0x00;

			  if(FLAG_UART_1)
			  {
				  FLAG_UART_1 = 0x00;
			  	  HAL_UART_Receive_DMA(&huart1, cmd, 3);
			  }
			  break;
	  }

	  if (FLAG_BAT)
	  {
		  HAL_TIM_Base_Stop_IT(&htim16);
		  htim16.Instance->CNT = 0x0;

		  BAT = Battery_Meas(&hadc);

		  if (BAT < 60)
			  HAL_TIMEx_PWMN_Start(&htim17, TIM_CHANNEL_1);
		  else HAL_TIMEx_PWMN_Stop(&htim17, TIM_CHANNEL_1);

		  FLAG_BAT = 0x00;
		  HAL_TIM_Base_Start_IT(&htim16);
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

